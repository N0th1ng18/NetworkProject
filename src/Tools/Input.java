package Tools;

public class Input {

	private int W;
	private int A;
	private int S;
	private int D;
	
	public Input() {
		W = 0;
		A = 0;
		S = 0;
		D = 0;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

	public int getW() {
		return W;
	}

	public void setW(int w) {
		W = w;
	}

	public int getA() {
		return A;
	}

	public void setA(int a) {
		A = a;
	}

	public int getS() {
		return S;
	}

	public void setS(int s) {
		S = s;
	}

	public int getD() {
		return D;
	}

	public void setD(int d) {
		D = d;
	}
	
	public void resetAll() {
		W = 0;
		A = 0;
		S = 0;
		D = 0;
	}
	
	
}
